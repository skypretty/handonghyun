        document.querySelectorAll('.object-rule-section').forEach(function (section) {
            const scroll = section.querySelector('.object-rule-scroll');
            if (!scroll) return;

            let curX = 0, isDragging = false, startX = 0, startCurX = 0;
            let velX = 0, lastX = 0, moved = false, rafId = null;

            function totalW() {
                const cards = scroll.querySelectorAll('.object-rule-card');
                let w = 0;
                cards.forEach((c, i) => { w += c.offsetWidth + (i < cards.length - 1 ? 16 : 0); });
                return w;
            }

            function getMin() {
                return -(totalW() - section.getBoundingClientRect().width);
            }

            function clamp(v) {
                return Math.max(getMin(), Math.min(0, v));
            }

            function apply(x) {
                curX = clamp(x);
                scroll.style.transform = `translateX(${curX}px)`;
            }

            scroll.addEventListener('mousedown', e => {
                isDragging = true; moved = false; velX = 0;
                startX = e.pageX; startCurX = curX; lastX = e.pageX;
                cancelAnimationFrame(rafId);
                scroll.classList.add('is-dragging');
            });

            window.addEventListener('mousemove', e => {
                if (!isDragging) return;
                const dx = e.pageX - startX;
                if (Math.abs(dx) > 4) moved = true;
                velX = e.pageX - lastX;
                lastX = e.pageX;
                apply(startCurX + dx);
            });

            window.addEventListener('mouseup', () => {
                if (!isDragging) return;
                isDragging = false;
                scroll.classList.remove('is-dragging');
                (function momentum() {
                    if (Math.abs(velX) < 0.5) return;
                    const min = getMin();
                    const next = curX + velX;
                    if (next <= min) {
                        curX = min;
                        scroll.style.transform = `translateX(${curX}px)`;
                        return;
                    }
                    apply(next);
                    velX *= 0.92;
                    rafId = requestAnimationFrame(momentum);
                })();
            });

            scroll.addEventListener('click', e => { if (moved) e.stopPropagation(); }, true);
            scroll._wasMoved = () => moved;
        });

